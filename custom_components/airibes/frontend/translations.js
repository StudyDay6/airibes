export function getTranslation(language) {
    return translations[language] || translations['en'];
}

/**
 * 通过key获取翻译值
 * @param {string} key - 翻译键
 * @param {string} [language] - 语言代码，如果未提供则使用英语
 * @param {string} [defaultValue] - 如果找不到翻译，返回的默认值
 * @returns {string} 翻译值或默认值或key本身
 */
export function getTranslationValue(key, language = 'en', defaultValue = undefined) {
    try {
        // 获取指定语言的翻译
        const trans = translations[language] || translations['en'];
        
        // 如果找到翻译值则返回，否则返回默认值或key本身
        return trans[key] || defaultValue || key;
    } catch (error) {
        console.error(`获取翻译失败 [${key}]:`, error);
        return defaultValue || key;
    }
}



